// Define the numbers and the operation here
let Num1 = 5;
let Num2 = 3;
let operator = "+";
let result;

// Write if-statements here

if (operator === "+") {
  result = Num1 + Num2;
} else if (operator === "-") {
  result = Num1 - Num2;
} else if (operator === "/") {
  result = Num1 / Num2;
} else if (operator === "*") {
  result = Num1 * Num2;
}
console.log(result);
